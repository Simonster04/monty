#include "monty.h"

/**
 * stack_sub - subtracts the top of the stack from the 2nd top
 * @stack: array with data
 * @line_number: line number
 */
void stack_sub(stack_t **stack, unsigned int line_number)
{
	int sum;

	if (stack == NULL || *stack == NULL || (*stack)->next == NULL)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", line_number);
		exit(EXIT_FAILURE);
	}

	(*stack) = (*stack)->next;
	sum = (*stack)->n - (*stack)->prev->n;
	(*stack)->n = sum;
	free((*stack)->prev);
	(*stack)->prev = NULL;
}


/**
 * stack_div - divides the second top of the stack by the top
 * @stack: array with data
 * @line_number: line number
 */
